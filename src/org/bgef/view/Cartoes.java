    private void jButtonAddCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddCActionPerformed

        DefaultListModel dlmA = (DefaultListModel) jList1.getModel();
        DefaultListModel dlm2A = (DefaultListModel) jList2.getModel();
        DefaultListModel dlmV = (DefaultListModel) jList3.getModel();
        DefaultComboBoxModel dcJogadores = (DefaultComboBoxModel) jComboBoxJogadores.getModel();

        Jogador j = (Jogador) jComboBoxJogadores.getSelectedItem();

        if (((String) jComboBoxCartoes.getSelectedItem()).compareTo("Amarelo") == 0) {

            if (j != null) {
                dlmA.addElement(j);
                dcJogadores.removeElement(j);
            }

        } else if (((String) jComboBoxCartoes.getSelectedItem()).compareTo("Duplo Amarelo") == 0) {

            if (j != null) {
                dlm2A.addElement(j);
                dcJogadores.removeElement(j);
            }
        } else //vermelho
        {

            if (j != null) {
                dlmV.addElement(j);
                dcJogadores.removeElement(j);
            }
        }
    }//GEN-LAST:event_jButtonAddCActionPerformed

