    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        DefaultListModel dlmA = (DefaultListModel) jList1.getModel();
        DefaultListModel dlm2A = (DefaultListModel) jList2.getModel();
        DefaultListModel dlmV = (DefaultListModel) jList3.getModel();

        int sizea = dlmA.size();
        int size2a = dlm2A.size();
        int sizev = dlmV.size();

        for (int i = 0; i < sizea; i++) {
            Jogador j = (Jogador) dlmA.getElementAt(i);
            Cartao c = new Cartao(j.getId(), j, this.idJogo, "Amarelo");
            this.nova.addCartao(c);
        }

        for (int i = 0; i < size2a; i++) {
            Jogador j = (Jogador) dlm2A.getElementAt(i);
            Cartao c = new Cartao(j.getId(), j, this.idJogo, "Amarelo");
            Cartao c2 = new Cartao(j.getId(), j, this.idJogo, "Vermelho");
            this.nova.addCartao(c);
            this.nova.addCartao(c);
            this.nova.addCartao(c2);
        }

        for (int i = 0; i < sizev; i++) {
            Jogador j = (Jogador) dlmV.getElementAt(i);
            Cartao c = new Cartao(j.getId(), j, this.idJogo, "Vermelho");
            this.nova.addCartao(c);
        }

        dlmA.removeAllElements();
        dlm2A.removeAllElements();
        dlmV.removeAllElements();

        this.dispose();
    }//GEN-LAST:event_jButtonInserirActionPerformed

